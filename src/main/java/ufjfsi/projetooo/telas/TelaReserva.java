    private void jButtonReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonReservarActionPerformed

        Reserva r = new Reserva();
        r.setClienteFis(clientesFis.get(Integer.parseInt(jTextFieldCliente1.getText())));
        r.setVeiculo(frota.get(Integer.parseInt(jTextFieldVeiculo1.getText())));
        r.getVeiculo().setAlugado(true);
        r.setPeriodo(Integer.parseInt(jTextFieldPeriodo.getText()));
        reserva.add(r);

        if (r.getPeriodo() > 365) {
            caixa.add(r.getPeriodo() * 0.7 * r.getVeiculo().getValor());
            credito.add(r.getPeriodo() * 0.7 * r.getVeiculo().getValor());
        } else if (r.getPeriodo() > 30) {
            caixa.add(r.getPeriodo() * 0.9 * r.getVeiculo().getValor());
            credito.add(r.getPeriodo() * 0.9 * r.getVeiculo().getValor());

        } else {
            caixa.add(r.getPeriodo() * r.getVeiculo().getValor());
            credito.add(r.getPeriodo() * r.getVeiculo().getValor());
        }

        TelaListaReservas telaR = new TelaListaReservas();
        telaR.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        telaR.setVisible(true);
        dispose();
    }//GEN-LAST:event_jButtonReservarActionPerformed

